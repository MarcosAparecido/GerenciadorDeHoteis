    private void criarHospede() {
        DatasUtil datasUtil = new DatasUtil();
        String nome = txtNome.getText();
        String cpf = txtCpf.getText();
        String passaporte = txtPassaporte.getText().toUpperCase();
        String email = txtEmail.getText();
        String telefone = txtTelefone.getText();
        String pais = (String) cmbPais.getSelectedItem();
        Date dataNascimento = datasUtil.converterStringParaData(txtDataNascimento.getText());
        Hospede hospede = new Hospede(nome, cpf, passaporte, email, telefone, pais, dataNascimento);
        tipoService.salvarHospede(hospede);
        setCpfHospede(cpf);
        setPassaporteHospede(passaporte);
        limparCampos();
    }

    private void abrirTelaCheckInCheckOut() {
        String cpf = getCpfHospede();
        String passaporte = getPassaporteHospede();
        if (cpf != null && passaporte != null) {
            GerenciamentoCheckInCheckOut checkInCheckOut = new GerenciamentoCheckInCheckOut(getCpfFuncionario(), cpf, passaporte);
            checkInCheckOut.setVisible(true);

        } else if (cpf != null && passaporte == null) {
            GerenciamentoCheckInCheckOut checkInCheckOut = new GerenciamentoCheckInCheckOut(getCpfFuncionario(), cpf, null);
            checkInCheckOut.setVisible(true);

        } else if (passaporte != null && cpf == null) {
            GerenciamentoCheckInCheckOut checkInCheckOut = new GerenciamentoCheckInCheckOut(getCpfFuncionario(), null, passaporte);
            checkInCheckOut.setVisible(true);

        } else {
            GerenciamentoCheckInCheckOut checkInCheckOut = new GerenciamentoCheckInCheckOut(getCpfFuncionario(), null, null);
            checkInCheckOut.setVisible(true);

        }
        this.dispose();
    }

